升平署脸谱
描述
该相册再现了京剧脸谱艺术。 该绘画集是清朝清咸丰、同治（1851—1874）以后宫廷画师的作品的一名宫廷画师的作品。
清朝成立了升平署，管理剧团事务。 举行御前演出和庆典仪式时，该办事处就负责戏剧演出。 演员的妆容一般都遵循统一的脸谱和颜色。
该画集中的 97 幅图片展现了 9 种不同戏剧演出的妆容。 插图精美，颜色新鲜，无论是丝绸背景还是颜料都称得上是最高品质。
不出意料，该绘画集是“皇家的玩赏之物”。此绘画集因其艺术造诣和传达的信息而备受推崇，是学习早期京剧服装和脸谱宝贵的历史资源。
